                                <p>Bohužel se řadíme mezi rodiny, u kterých selhal odborný personál porodnice. </p>
                            </blockquote>

                            <p class="wow fadeInUp" data-wow-delay="0.6s">Moje dcera se přidusila během porodu, byla oživována a během této doby se jí nenávratně poškodil mozek. Nikdo nám situaci nevysvětlil, a i když jsme tušili, že je zle, všichni nás po dobu hospitalizace utěšovali, že bude vše v pořádku. Při propuštění mi bylo sděleno, že se dokumentace z porodu záhadně ztratila, a tak jsme měli jasno. Paulínce byly dva měsíce a my začali jednat.

                                I kdybychom tušili, že čekáme dítě s těžkým postižením, na tento fakt vás vůbec nic nepřipraví. Neexistuje prenatální kurz, psychologická poradna, odborná literatura na téma „čekáme hendikepované dítě“. Pečujícím se tedy stanete z nenadání a jste nepřipraven.</p>

                            <h2 class="text-anime-style-2">Jaké byly začátky?</h2>

                            <p class="wow fadeInUp" data-wow-delay="0.8s">Paulínce je dnes 24 let a začátky vnímám hodně těžké po psychické stránce. Nejtěžší bylo vyrovnat se s její diagnózou, nevinit se ze selhání, komplikované hledání informací a taky nedostačující odborná pomoc. Dnes je její postižení, pro nás, náročnější po té fyzické stránce a vnímáme své limity, které se budou časem jenom snižovat.</p>


                            <h2 class="text-anime-style-2">Handicap Vaší dcery ovlivňuje život Vás, ale i celé rodiny. Jak vypadá Váš den?</h2>

                            <p class="wow fadeInUp" data-wow-delay="0.8s">To je dobrá otázka, ale těžce se na ní odpovídá. Péče o dceru se stala za ty léta naším standardem. Pracujeme s faktem, že situace je neměnná, a tak si vnitřně nepřipouštíme žádná omezení. Jednoduše pracujeme s tím, co můžeme a nemůžeme.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Kdybych se podívala na náš denní život z pohledu nestranného pozorovatele, tak vnímám jednotvárnou každodennost, která se v čase neměnní. Máme doma „mimino“, které je zapotřebí denně obléct, převléct, okoupat, několikrát nakrmit, přenést, odvést do „školky“ – stacionáře, včas jej vyzvednout, myslet na její nálady, a v případě výletu na její kola invalidního vozíku. Nikdy nesmíme zapomenout mít s sebou láhev se silikonovou savičkou, dostatek bryndáků a plenu na přebalení. A to je jí 24 let!</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Těžce se cokoliv plánuje, ale to neznamená, že sedíme jenom doma. Její vrstevníci už možná nežijí u rodičů, studují, nebo pracují, jsou samostatným jedincem. Vy pracujete, zajdete si s přáteli na večeři, s partnerem do kina, zaletíte si na dovolenou nebo si lehnete kdykoliv cítíte únavu – to vše je u nás nemožné.</p>
                        
                            <h2 class="text-anime-style-2">Založila jste značku Needo, která se zaměřuje na pomůcky pro osoby s postižením. Šijete bryndáky, šátky, body ale také bundy, aby se daly lépe obléct osobám s omezenou pohyblivostí. Kdy vznikla myšlenka se o něco takového pokusit?</h2>

                            <p class="wow fadeInUp" data-wow-delay="0.8s">Značku NEEDO jsem nosila v hlavě od momentu, kdy jsem neúspěšně hledala jakékoli pomůcky pro dceru, která je ležící a spastická. Trh s pomůckami je hodně omezený a nutí vás přijímat kompromisní řešení.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Před čtyřmi lety jsme se s manželem vrhli do nelehkého sociálně-prospěšného podnikání a zacílili jsme se na počtem limitující skupinu zákazníků – pečující a centra pro pobyt lidí s hendikepem.  Navázala jsem spolupráci s chráněnou šicí dílnou, aby naše pomůcky pomáhali od samého začátku.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Dnes Needo nabízí přes 400 unikátních druhů textilních pomůcek pro děti, dospívající a dospělé, ve formě:</p>

                            <ul class="wow fadeInUp" data-wow-delay="1s">
                                <li>funkčních šátků (bryndáků) v různých střizích a motivech, ve výběru ve více velikostech;</li>
                                <li>bodytrika až do výšky 188 cm, která ochrání záda před nachlazením a udrží plenu na svém místě. Ty ocení zejména lidi sedící na invalidním vozíku. Nabízíme je v bavlně nebo bambusu, který má skvělé termo vlastnosti a díky své elasticitě se snadněji obléká na člověka, který se sám obléct nedokáže;</li>
                                <li>bundy s postranními zipy. Ty se zcela otevřou a bunda se snadno obleče a zapne přímo na sedícím nebo ležícím člověku;</li>
                                <li>spodního prádla nebo plavek s ochranou při inkontinenci. Obojí působí navenek velice slušivě, a tak okolí v případě plavek nepozná, že má člověk problém s únikem;</li>
                                <li>tepláky s ochranou kolen a vyšším klínem, aby seděli hezky na pase i během manipulace a sezení;</li>
                                <li>rukavice na spastické ruce;</li>
                                <li>pomůcek na polohování, spaní nebo manipulaci.</li>
                            </ul>

                            <p class="wow fadeInUp" data-wow-delay="0.8s">Celý sortiment najdete <a href="https://www.needo.cz/" target="_blank">zde</a>.</p>

                            <h2 class="text-anime-style-2">Natáčíte vlastní podcast. Můžete ho čtenářům přiblížit?</h2>

                            <p class="wow fadeInUp" data-wow-delay="0.8s">Cílem NEEDO je naplnit tři pilíře – poskytnout unikátní pomůcky, finanční pomoc pro pečující a relevantní informace, a tak vznikla myšlenka sdílet informace, zkušenosti a novinky přes mluvené slovo formou podcastů.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Oslovila jsem kamarádku Lenku, která je také pečující. Shodly jsme se na tom, že podcasty budeme točit do doby, kdy bude dostatek hostů, témat a nadšení to dělat.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Do podcastů NEEDo TALKs zveme hosty, kteří mají, co říct na téma „postižení“ a dáváme jim hodinový prostor sdílet své zkušenosti a tím pomoct někomu dalšímu. Jsou to životní osudy pečujících, lidí s hendikepem, institucí, nadací, rodinných příslušníků. Podle ohlasů víme, že se mnoho pečujících a pracovníků v různých organizacích, najde v každém dílu. Informace mohou pomoci se posunout nebo si jednoduše poslechnete, že někdo další řeší podobné problémy jako vy. Pocit ztroskotance na opuštěném ostrově totiž spojuje všechny pečující.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Není to lehké téma, ale věříme, že poslech našeho podcastu zasáhne i posluchače, pro které toto téma není úplně aktuální. Je totiž důležité vědět, co se děje za dveřmi pečujících rodin, aby jim dokázalo okolí pomoct, chovat se adekvátně a připravit své děti na možnost se potkat s vrstevníkem s hendikepem.</p>

                            <h2 class="text-anime-style-2">Na jaký díl nebo hosta podcastu nejraději vzpomínáte? Který, pro Vás, byl naopak nejtěžší?</h2>

                            <p class="wow fadeInUp" data-wow-delay="0.8s">Mám ráda každý díl. Vždy se něco nového dozvím; něco, co můžu sama aplikovat při péči.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Je tedy těžké z těch sto dílů, které jsme natočily, vybrat jen jeden, který v nás nejvíce rezonuje. Pro mě osobně to, ale rozhodně je rozhovor s mojí druhou dcerou Kristínkou, se kterou si povídala Lenka o tom, jaké je vyrůstat jako „ta druhá“. Dojalo mě, jak silná osobnost z ní vyrostla.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Ty nejtěžší témata jsou však spojeny se smrtí, jasnou definici konce, kdy za mikrofonem sedí člověk, který by obrátil svět, aby tento osud zvrátil.</p>

                            <h2 class="text-anime-style-2">Jste opravdu akční. Máte ale čas i na odpočinek? Jak odpočíváte?</h2>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Je pravdou, že jenom tak sedět nebo ležet nedokážu. Jsem kreativní člověk a každý den je pro mě krátký.</p>
                            <p class="wow fadeInUp" data-wow-delay="0.8s">Spolu s NEEDO, který vznikl pro Paulínku, vedu další a pomáhám mladší dceři Kristínce s <a href="https://www.madebykristina.cz/">její webovou stránkou</a>. Pomáhat obou mým holkám mi dává obrovský smysl, a tak to neberu jako práci, ale jako zábavu. Pracuji tak s rodinou a pomáháme si navzájem. Bez nich bych to nezvládla.</p>


